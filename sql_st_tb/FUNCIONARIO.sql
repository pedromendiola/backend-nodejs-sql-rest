		CONSTRAINT FUN_CK_FUN_NUMERO CHECK (FUN_IN_NUMERO >=0),
FUN_RE_SALARIO NUMERIC(12,2) NOT NULL CONSTRAINT FUN_DF_FUN_SALARIO DEFAULT 0
		CONSTRAINT FUN_CK_FUN_SALARIO CHECK (FUN_RE_SALARIO >=0),
)


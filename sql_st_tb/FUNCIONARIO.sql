			or FUN_CH_REGISTRO LIKE ('[0-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9]')),
FUN_ST_NOME VARCHAR(70) NOT NULL,
FUN_ST_TIPO VARCHAR(50) NOT NULL,
FUN_DT_INCLUSAO DATE NOT NULL CONSTRAINT FUN_DF_FUN_INCLUSAO DEFAULT GETDATE(),
FUN_DT_DATAADMISSAO DATE NOT NULL,

FUN_RE_VALOR NUMERIC(12,2) NOT NULL CONSTRAINT FUN_DF_FUN_VALOR DEFAULT 0
		CONSTRAINT FUN_CK_FUN_VALOR CHECK (FUN_RE_VALOR >=0),
